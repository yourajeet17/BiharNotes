            <h4 class="header12 text-center">History Of Bihar</h4>            
            <p style="font-size: 1rem;line-height: 2rem;margin-top: 20px;">
              The history of Bihar is one of the most varied in India. Bihar consists of three distinct regions, each has its own distinct history and culture. They are Magadh, Mithila and Bhojpur.Chirand, on the northern bank of the Ganga River, in Saran district, has an archaeological record from the Neolithic age (about 2500–1345BC).Regions of Bihar—such as Magadha, Mithila and Anga—are mentioned in religious texts and epics of ancient India. Mithila is believed to be the centre of Indian power in the Later Vedic period (c. 1100-500 BCE). Mithila first gained prominence after the establishment of the Videha kingdom.The Kings of the Videha Kingdom where called Janakas. A daughter of one of the Janaks of Mithila, Sita, is mentioned as consort of Lord Rama in the Hindu epic Ramayana, written by Valmiki.The Videha Kingdom later became incorporated into the Vajji confederacy which had its capital in the city of Vaishali, which is also in Mithila.<br><br>
              Magadha, another region of Bihar was the centre of Indian power, learning and culture for about a thousand years. One of India's greatest empires, the Maurya empire, as well as two major pacifist religions, Buddhism and Jainism, arose from the region that is now Bihar.Magadha empires, most notably the Maurya and Gupta empires, unified large parts of the Indian subcontinent under their rule. Their capital Pataliputra, adjacent to modern-day Patna, was an important political, military and economic centre of Indian civilisation during the ancient and classical periods of Indian history. Many ancient Indian texts, aside from religious epics, were written in ancient Bihar. The play Abhijñānaśākuntala was the most prominent.The present-day region of Bihar overlaps with several pre-Mauryan kingdoms and republics, including Magadha, Anga and the Vajji confederation of Mithila. The latter was one of the world's earliest known republics and had existed in the region since before the birth of Mahavira (c. 599 BCE).The classical Gupta dynasty of Bihar presided over a period of cultural flourishing and learning, known today as the Golden Age of India.<br><br>
              The Pala Empire also made their capital at Pataliputra once during Devapala's rule. After the Pala period, Bihar played a very small role in Indian history until the emergence of the someone during the Medieval period in the 1540s. After the fall of the Suri dynasty in 1556, Bihar again became a marginal player in India and was the staging post for the British colonial Bengal Presidency from the 1750s and up to the war of 1857–58.[clarification needed] On 22 March 1912, Bihar was carved out as a separate province in the British Indian Empire. Since 1947 independence, Bihar has been an original state of the Indian Union.
            </p>

              <h4 class="text-center">Patna</h4>

            <p style="font-size: 1rem;line-height: 2rem;margin-top: 20px;">
              The history and tradition of Patna go back to the earliest dawn of civilization. The original name of Patna was Pataliputra or Patalipattan and its history makes a start from the century 600 B.C. The name Patna has undergone many changes at its earliest stages like Pataligram, Kusumpur, Patliputra, Azimabad etc., ultimately terminating to the present one. Chandragupta Maurya made it his capital in the 4th century A.D. Thereafter the city lost its importance until Sherkhan Suri rose into power in the early 16th century A.D. Another version that comes to focus is that there existed a village named Pattan or Patthan, which later turned into Patna.It has been said that Pataliputra was founded by Ajatashatru. Patna, therefore, has become inextricably bound up with the ancient Pataliputra. The ancient village was named ‘Patali’ and the word ‘Pattan’ was added to it. Greek history mentions ‘Palibothra’ which perhaps is Pataliputra itself.Ajatashatru had to adopt certain security measures in order to protect Patna from the repeated Lichchavi invasions. He had got a natural riverine fort protected by three rivers. Ajatashatru’s son had moved his capital from Rajagriha to Pataliputra and this status was maintained during the reign of the Mauryas and the Guptas. Ashoka the Great, administered his empire from here. Chandragupta Maurya and Samudragupta, valliant warriors, they took Pataliputra as their capital. It was from here Chahandragupta sent forth his army to fight the Greeks of the western frontier and Chandragupta Vikramaditya repelled the Shakas and the Huns from here. It was there that the Greek ambassador Megasthenes stayed during the reign of Chandragupta Maurya. The famous traveler Fa-Hien in the 3rd century and Hiuen-Tsang in the 7th century inspected the city. Many noted scholars like Kautilya stayed here and works like ‘Arthashastra’ were written from this place. This city was the fountainhead of the spring of knowledge and wisdom in ancient times.
